package helper;
/**
 *  classe usada para indicar uma determinada posição no labirinto 
 * @author GuiBode
 *
 */
public class Posicao {

	private int linha , coluna ;

	public int getLinha() {
		return linha;
	}

	public void setLinha(int linha) {
		this.linha = linha;
	}

	public int getColuna() {
		return coluna;
	}

	public void setColuna(int coluna) {
		this.coluna = coluna;
	}
	
	
}
